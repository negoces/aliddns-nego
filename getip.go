func verifyNetConn(test_url string) (int, error) {
	startTime := time.Now()
	_, err := http.Get(test_url)
	elapsedTime := time.Since(startTime).Milliseconds()
	if err != nil {
		return 0, err
	}
	return int(elapsedTime), nil
}

func getIP(api_url string, var_ip *string, reg string) error {
	re, err := regexp.Compile(reg)
	if err != nil {
		return err
	}
	res, err := http.Get(api_url)
	if err != nil {
		defer res.Body.Close()
		return err
	}
	ip, _ := io.ReadAll(res.Body)
	defer res.Body.Close()
	*var_ip = string(re.Find(ip))
	return nil
}
